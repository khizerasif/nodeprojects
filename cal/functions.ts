function sum(num1: number, num2: number) {
    let sum = num1+num2
    return sum
}
export { sum }


function sub(num1:number, num2:number)
{
    let sub = num1-num2
    return sub
}
export { sub }

function mul(num1:number, num2:number) {
    let mul = num1*num2
    return mul
}
export { mul }

function dvd(num1:number, num2:number) {
    let dvd = num1/num2
    return dvd
}
export { dvd }
