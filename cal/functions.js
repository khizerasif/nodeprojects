function sum(num1, num2) {
    let sum = num1 + num2;
    return sum;
}
export { sum };
function sub(num1, num2) {
    let sub = num1 - num2;
    return sub;
}
export { sub };
function mul(num1, num2) {
    let mul = num1 * num2;
    return mul;
}
export { mul };
function dvd(num1, num2) {
    let dvd = num1 / num2;
    return dvd;
}
export { dvd };
